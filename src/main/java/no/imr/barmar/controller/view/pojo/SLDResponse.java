package no.imr.barmar.controller.view.pojo;

public class SLDResponse {
	private String sldFilename;
	private float maxLegend;
	private float minLegend;
	
	public String getSldFilename() {
		return sldFilename;
	}
	public void setSldFilename(String sldFilename) {
		this.sldFilename = sldFilename;
	}
	public float getMaxLegend() {
		return maxLegend;
	}
	public void setMaxLegend(float maxLegend) {
		this.maxLegend = maxLegend;
	}
	public float getMinLegend() {
		return minLegend;
	}
	public void setMinLegend(float minLegend) {
		this.minLegend = minLegend;
	}
	
	
}
